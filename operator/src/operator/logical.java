package operator;

public class logical {

	public static void main(String[] args) {
		boolean bol1 = false, bol2 = false;
		
		System.out.println("bol1 && bol2 =" +(bol1&&bol2));
		System.out.println("bol1 || bol2 =" +(bol1||bol2));
        System.out.println("!(bol1 && bol2) =" +!(bol1&&bol2));
	}

}
